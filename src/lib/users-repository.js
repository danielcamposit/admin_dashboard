import "server-only";
import { getSql } from "./db";

function mapUser(row) {
  return {
    id: Number(row.id),
    name: row.name,
    email: row.email,
    address: {
      city: row.city || "",
    },
    company: {
      name: row.company || "",
    },
    createdAt: row.created_at,
  };
}

function normalizeUserInput(user) {
  const name = user?.name?.trim();
  const email = user?.email?.trim()?.toLowerCase();
  const city = user?.address?.city?.trim() || "";
  const company = user?.company?.name?.trim() || "";

  if (!name) {
    throw new Error("Name is required.");
  }

  if (!email) {
    throw new Error("Email is required.");
  }

  return { name, email, city, company };
}

async function ensureUsersTable() {
  const sql = getSql();

  await sql`
    CREATE TABLE IF NOT EXISTS users (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL,
      email TEXT NOT NULL UNIQUE,
      city TEXT DEFAULT '',
      company TEXT DEFAULT '',
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `;
}

async function seedUsersIfEmpty() {
  const sql = getSql();
  const countResult = await sql`SELECT COUNT(*)::int AS count FROM users`;

  if (countResult[0]?.count > 0) {
    return;
  }

  await sql`
    INSERT INTO users (name, email, city, company)
    VALUES
      ('Leanne Graham', 'leanne@example.com', 'Gwenborough', 'Romaguera-Crona'),
      ('Ervin Howell', 'ervin@example.com', 'Wisokyburgh', 'Deckow-Crist'),
      ('Clementine Bauch', 'clementine@example.com', 'McKenziehaven', 'Romaguera-Jacobson'),
      ('Patricia Lebsack', 'patricia@example.com', 'South Elvis', 'Robel-Corkery')
  `;
}

async function ensureDataReady() {
  await ensureUsersTable();
  await seedUsersIfEmpty();
}

export async function getAllUsers() {
  const sql = getSql();
  await ensureDataReady();

  const rows = await sql`
    SELECT id, name, email, city, company, created_at
    FROM users
    ORDER BY created_at DESC, id DESC
  `;

  return rows.map(mapUser);
}

export async function createUser(user) {
  const sql = getSql();
  const { name, email, city, company } = normalizeUserInput(user);
  await ensureUsersTable();

  try {
    const rows = await sql`
      INSERT INTO users (name, email, city, company)
      VALUES (${name}, ${email}, ${city}, ${company})
      RETURNING id, name, email, city, company, created_at
    `;

    return mapUser(rows[0]);
  } catch (error) {
    if (error?.code === "23505") {
      throw new Error("Email already exists.");
    }

    throw error;
  }
}

export async function updateUserById(id, user) {
  const sql = getSql();
  const { name, email, city, company } = normalizeUserInput(user);
  await ensureUsersTable();

  try {
    const rows = await sql`
      UPDATE users
      SET
        name = ${name},
        email = ${email},
        city = ${city},
        company = ${company}
      WHERE id = ${id}
      RETURNING id, name, email, city, company, created_at
    `;

    if (!rows[0]) {
      throw new Error("User not found.");
    }

    return mapUser(rows[0]);
  } catch (error) {
    if (error?.code === "23505") {
      throw new Error("Email already exists.");
    }

    throw error;
  }
}

export async function deleteUserById(id) {
  const sql = getSql();
  await ensureUsersTable();

  const rows = await sql`
    DELETE FROM users
    WHERE id = ${id}
    RETURNING id
  `;

  if (!rows[0]) {
    throw new Error("User not found.");
  }
}
