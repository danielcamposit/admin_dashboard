import "server-only";
import { getSql } from "./db";
import { hashPassword, verifyPassword } from "./password";

function mapAuthUser(row) {
  return {
    id: Number(row.id),
    name: row.name,
    email: row.email,
    role: row.role,
    createdAt: row.created_at,
  };
}

function normalizeAuthUserInput(user) {
  const name = user?.name?.trim();
  const email = user?.email?.trim()?.toLowerCase();
  const password = user?.password || "";

  if (!name) {
    throw new Error("Name is required.");
  }

  if (!email) {
    throw new Error("Email is required.");
  }

  if (!password) {
    throw new Error("Password is required.");
  }

  return { name, email, password };
}

async function ensureAuthUsersTable() {
  const sql = getSql();

  await sql`
    CREATE TABLE IF NOT EXISTS auth_users (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL,
      email TEXT NOT NULL UNIQUE,
      role TEXT NOT NULL DEFAULT 'user',
      password_hash TEXT NOT NULL,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `;

  await sql`
    ALTER TABLE auth_users
    ADD COLUMN IF NOT EXISTS role TEXT NOT NULL DEFAULT 'user'
  `;

  const adminCount = await sql`
    SELECT COUNT(*)::int AS count
    FROM auth_users
    WHERE role = 'admin'
  `;

  if ((adminCount[0]?.count || 0) === 0) {
    await sql`
      WITH first_user AS (
        SELECT id
        FROM auth_users
        ORDER BY created_at ASC, id ASC
        LIMIT 1
      )
      UPDATE auth_users
      SET role = 'admin'
      WHERE id IN (SELECT id FROM first_user)
    `;
  }
}

export async function registerAuthUser(user) {
  const sql = getSql();
  const { name, email, password } = normalizeAuthUserInput(user);
  const passwordHash = hashPassword(password);
  await ensureAuthUsersTable();
  const countResult = await sql`SELECT COUNT(*)::int AS count FROM auth_users`;
  const role = (countResult[0]?.count || 0) === 0 ? "admin" : "user";

  try {
    const rows = await sql`
      INSERT INTO auth_users (name, email, role, password_hash)
      VALUES (${name}, ${email}, ${role}, ${passwordHash})
      RETURNING id, name, email, role, created_at
    `;

    return mapAuthUser(rows[0]);
  } catch (error) {
    if (error?.code === "23505") {
      throw new Error("Email already exists.");
    }

    throw error;
  }
}

export async function authenticateUser(email, password) {
  const sql = getSql();
  const normalizedEmail = email?.trim()?.toLowerCase();

  if (!normalizedEmail || !password) {
    throw new Error("Email and password are required.");
  }

  await ensureAuthUsersTable();

  const rows = await sql`
    SELECT id, name, email, role, password_hash, created_at
    FROM auth_users
    WHERE email = ${normalizedEmail}
    LIMIT 1
  `;

  const user = rows[0];

  if (!user || !verifyPassword(password, user.password_hash)) {
    throw new Error("Invalid credentials.");
  }

  return mapAuthUser(user);
}

export async function deleteAuthUserById(id) {
  const sql = getSql();
  await ensureAuthUsersTable();

  const deletedRows = await sql`
    DELETE FROM auth_users
    WHERE id = ${id}
    RETURNING id, role
  `;

  const deletedUser = deletedRows[0];

  if (!deletedUser) {
    throw new Error("User not found.");
  }

  if (deletedUser.role === "admin") {
    const adminCount = await sql`
      SELECT COUNT(*)::int AS count
      FROM auth_users
      WHERE role = 'admin'
    `;

    if ((adminCount[0]?.count || 0) === 0) {
      await sql`
        WITH replacement AS (
          SELECT id
          FROM auth_users
          ORDER BY created_at ASC, id ASC
          LIMIT 1
        )
        UPDATE auth_users
        SET role = 'admin'
        WHERE id IN (SELECT id FROM replacement)
      `;
    }
  }
}
